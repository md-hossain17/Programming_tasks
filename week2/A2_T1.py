# Program starting
print("Program starting.\n")

# Ask for name
name = input("What is your name: ")

# Ask for two floating point numbers
num1 = float(input("Enter a floating point number: "))
num2 = float(input("Enter second floating point number: "))

# Display entered information
print(f"{name} you gave numbers {num1} and {num2}")

# Calculate product
product = num1 * num2
print(f"Multiplying first and second number will result in product {product}")

# Program ending
print("Program ending.")