# Assign values
Num1 = 47
Num2 = 102

# Perform calculations
Sum = Num1 + Num2
Diff = Num2 - Num1
Product = Sum * Diff

# Print operations
print(f"{Num1} + {Num2} = {Sum}")
print(f"{Num2} - {Num1} = {Diff}")
print(f"{Sum} * {Diff} = {Product}")

# Print all results together
print(f"\nResults: {Num1} + {Num2} = {Sum}, {Num2} - {Num1} = {Diff}, {Sum} * {Diff} = {Product}")